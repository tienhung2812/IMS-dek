    private void btnYESActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnYESActionPerformed
        // isUAS == true, peer này là UAS.
        if (phoneListener.isUAS()) {
            // phương thức sendResponse() tương ứng 
            // hành động bấm button YES của UAS 
            phoneListener.sendResponse();
        } // peer này là UAC
        else {
            // phương thức sendRequest() tương ứng 
            // hành động bấm button YES của UAC 
            phoneListener.sendRequest();
        }
    }//GEN-LAST:event_btnYESActionPerformed

    private void btnNOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNOActionPerformed
        // isUAS == true, peer này là UAS.
        if (phoneListener.isUAS()) {
            // phương thức terminateResponse() tương ứng 
            // hành động bấm button NO của UAS 
            phoneListener.terminateResponse();
        } // peer này là UAC
        else {
            // phương thức terminateRequest() tương ứng 
            // hành động bấm button NO của UASC
            phoneListener.terminateRequest();
        }
    }//GEN-LAST:event_btnNOActionPerformed

