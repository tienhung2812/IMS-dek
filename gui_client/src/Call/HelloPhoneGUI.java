    public void statusRegister(boolean b) {
        if (b) {
            this.btnConnect.setEnabled(false);
            this.btnYES.setEnabled(true);
            this.btnNO.setEnabled(true);
            this.btnDisconnect.setEnabled(true);
            lblStatusRegister.setIcon(new javax.swing.ImageIcon(getClass().getResource("/picture/Haha.png")));
        } else {
            this.btnConnect.setEnabled(true);
            this.btnYES.setEnabled(false);
            this.btnNO.setEnabled(false);
            this.btnDisconnect.setEnabled(false);
            lblStatusRegister.setIcon(new javax.swing.ImageIcon(getClass().getResource("/picture/Afterboom.png")));
        }
    }

